<?php
// Integer operations
$a = 10;
$b = 3;

// Addition
$sum = $a+ $b;

// Subtraction
$difference = $a - $b;

// Multiplication
$product = $a * $b;

// Division
$quotient_int = $a / $b;

// Modulus (remainder after division)
$remainder = $a % $b;

// Print results for integer operations
print("Integer Operations:\n");
print("Sum: " . $sum . "\n");
print("Difference: " . $difference . "\n");
print("Product: " . $product . "\n");
print("Quotient: " . $quotient . "\n");
print("Remainder: " . $remainder . "\n\n");

// Float operations
$a_float = 10.5;
$b_float = 3.2;

// Addition
$sum_float = $a_float + $b_float;

// Subtraction
$difference_float = $a_float - $b_float;

// Multiplication
$product_float = $a_float * $b_float;

// Division
$quotient_float = $a_float / $b_float;

// Print results for float operations
print("Float Operations:\n");
print("Sum: " . $sum_float . "\n");
print("Difference: " . $difference_float . "\n");
print("Product: " . $product_float . "\n");
print("Quotient: " . $quotient_float . "\n");
